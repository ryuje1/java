
public class Cal2 {
	
	void add(int a, int b, int c) {
		System.out.println(a+b+c);
	}
	
	void minus(int a, int b, int c) {
		System.out.println(a-b-c);
	}
	
	void multi(int a, int b, int c) {
		System.out.println(a*b*c);
	}
	
	void devide(int a, int b, int c) {
		System.out.println((double)a/b/c);
	}
	
	
}
